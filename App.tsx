const AuthScreen: React.FC<{ onLogin: (u: User) => void }> = ({ onLogin }) => {
  const [isLogin, setIsLogin] = useState(true);
  const [email, setEmail] = useState('');
  const [name, setName] = useState('');
  const [password, setPassword] = useState('');
  const [confirmPassword, setConfirmPassword] = useState('');
  const [error, setError] = useState('');

  const handleAuth = (e: React.FormEvent) => {
    e.preventDefault();
    setError('');

    if (!isLogin && password !== confirmPassword) {
        setError("Passwords do not match.");
        return;
    }

    const users: User[] = JSON.parse(localStorage.getItem('bf_users') || '[]');
    const hashedPassword = simpleHash(password);

    if (isLogin) {
      const user = users.find(u => u.email === email && u.passwordHash === hashedPassword);
      if (user) {
        localStorage.setItem('bf_session', JSON.stringify(user));
        onLogin(user);
      } else {
        setError('Invalid credentials or account does not exist.');
      }
    } else {
      if (users.find(u => u.email === email)) {
        setError('User already exists. Please login.');
        return;
      }
      const newUser: User = {
        id: Date.now().toString(),
        email,
        name: name || email.split('@')[0],
        passwordHash: hashedPassword
      };
      users.push(newUser);
      localStorage.setItem('bf_users', JSON.stringify(users));
      localStorage.setItem('bf_session', JSON.stringify(newUser));
      onLogin(newUser);
    }
  };

  const mockSocialLogin = (provider: string) => {
      alert(`${provider} login is mocked for this demo. Logging you in as a test user.`);
      const mockUser: User = {
          id: `social-${Date.now()}`,
          email: `user@${provider.toLowerCase()}.com`,
          name: `Test User (${provider})`,
          passwordHash: 'social-mock-hash'
      };
      localStorage.setItem('bf_session', JSON.stringify(mockUser));
      onLogin(mockUser);
  };

  return (
    <div className="min-h-screen flex items-center justify-center bg-gradient-to-br from-indigo-50 to-purple-50 p-4">
      <Card className="w-full max-w-md bg-white/80 backdrop-blur-xl border-white/50 shadow-2xl">
        <div className="text-center mb-8">
          <div className="inline-flex items-center justify-center w-16 h-16 rounded-full bg-gradient-to-tr from-indigo-500 to-purple-500 mb-4 shadow-lg shadow-indigo-200 animate-pulse">
            <Sparkles className="w-8 h-8 text-white" />
          </div>
          <h1 className="text-3xl font-bold bg-clip-text text-transparent bg-gradient-to-r from-slate-700 to-slate-900">
            BrandForge AI
          </h1>
          <p className="text-slate-500 mt-2">Craft your identity with generative intelligence.</p>
        </div>

        <form onSubmit={handleAuth} className="space-y-4">
          {!isLogin && (
            <Input 
              label="Full Name" 
              placeholder="John Doe" 
              value={name} 
              onChange={e => setName(e.target.value)} 
              required={!isLogin}
            />
          )}
          <Input 
            label="Email Address" 
            type="email" 
            placeholder="john@example.com" 
            value={email} 
            onChange={e => setEmail(e.target.value)}
            required 
          />
          <Input 
            label="Password" 
            type="password" 
            placeholder="••••••••" 
            value={password}
            onChange={e => setPassword(e.target.value)}
            required 
          />
          {!isLogin && (
              <Input 
                label="Confirm Password" 
                type="password" 
                placeholder="••••••••" 
                value={confirmPassword}
                onChange={e => setConfirmPassword(e.target.value)}
                required 
              />
          )}
          
          {error && <p className="text-red-500 text-sm text-center">{error}</p>}

          <Button type="submit" className="w-full py-3 text-lg">
            {isLogin ? 'Sign In' : 'Create Account'}
          </Button>
        </form>

        <div className="relative my-6">
            <div className="absolute inset-0 flex items-center">
                <span className="w-full border-t border-slate-200"></span>
            </div>
            <div className="relative flex justify-center text-xs uppercase">
                <span className="bg-white px-2 text-slate-500">Or continue with</span>
            </div>
        </div>

        <div className="grid grid-cols-2 gap-4">
            <Button variant="secondary" type="button" onClick={() => mockSocialLogin('Google')}>
                <span className="mr-2">G</span> Google
            </Button>
            <Button variant="secondary" type="button" onClick={() => mockSocialLogin('LinkedIn')}>
                <span className="mr-2 font-bold text-blue-700">in</span> LinkedIn
            </Button>
        </div>

        <div className="mt-6 text-center text-sm text-slate-500">
          {isLogin ? "Don't have an account? " : "Already have an account? "}
          <button 
            onClick={() => { setIsLogin(!isLogin); setError(''); }} 
            className="text-indigo-600 font-medium hover:underline"
          >
            {isLogin ? 'Sign up' : 'Log in'}
          </button>
        </div>
      </Card>
    </div>
  );
};

const ChatWidget: React.FC = () => {
  const [isOpen, setIsOpen] = useState(false);
  const [messages, setMessages] = useState<ChatMessage[]>([]);
  const [input, setInput] = useState('');
  const [isLoading, setIsLoading] = useState(false);
  const chatEndRef = useRef<HTMLDivElement>(null);
  const textareaRef = useRef<HTMLTextAreaElement>(null);

  // Load chat session from sessionStorage
  useEffect(() => {
      const saved = sessionStorage.getItem('bf_chat_session');
      if (saved) setMessages(JSON.parse(saved));
  }, []);

  // Save chat session
  useEffect(() => {
      if (messages.length > 0) {
        sessionStorage.setItem('bf_chat_session', JSON.stringify(messages));
      }
  }, [messages]);

  const sendMessage = async () => {
    if (!input.trim()) return;
    const userMsg: ChatMessage = { role: 'user', text: input, timestamp: Date.now() };
    setMessages(prev => [...prev, userMsg]);
    setInput('');
    setIsLoading(true);

    // Reset height
    if (textareaRef.current) textareaRef.current.style.height = 'auto';

    try {
        const history = messages.map(m => ({
            role: m.role === 'user' ? 'user' : 'model',
            parts: [{ text: m.text }]
        }));
        const response = await getChatResponse(history, userMsg.text);
        setMessages(prev => [...prev, { role: 'model', text: response || "I'm here to help.", timestamp: Date.now() }]);
    } catch (e) {
        setMessages(prev => [...prev, { role: 'model', text: "Sorry, I can't connect right now.", timestamp: Date.now() }]);
    } finally {
        setIsLoading(false);
        setTimeout(() => chatEndRef.current?.scrollIntoView({ behavior: 'smooth' }), 100);
    }
  };

  const handleInput = (e: React.ChangeEvent<HTMLTextAreaElement>) => {
      setInput(e.target.value);
      e.target.style.height = 'auto';
      e.target.style.height = `${e.target.scrollHeight}px`;
  };

  return (
    <div className="fixed bottom-6 right-6 z-50">
      {!isOpen && (
        <button 
          onClick={() => setIsOpen(true)}
          className="w-16 h-16 bg-gradient-to-r from-indigo-600 to-purple-600 rounded-full shadow-2xl flex items-center justify-center text-white hover:scale-105 transition-transform"
        >
          <MessageCircle className="w-8 h-8" />
        </button>
      )}
      
      {isOpen && (
        <div className="w-[350px] h-[500px] bg-white rounded-2xl shadow-2xl border border-slate-200 flex flex-col overflow-hidden animate-in slide-in-from-bottom-5">
          <div className="p-4 bg-slate-900 text-white flex justify-between items-center">
            <div className="flex items-center gap-2">
                <Sparkles className="w-5 h-5 text-yellow-400" />
                <span className="font-bold">Granite Consultant</span>
            </div>
            <button onClick={() => setIsOpen(false)} className="text-white/70 hover:text-white"><X className="w-5 h-5"/></button>
          </div>
          
          <div className="flex-1 overflow-y-auto p-4 space-y-4 bg-slate-50">
             {messages.length === 0 && (
                 <div className="text-center text-slate-400 text-sm mt-10">
                     <p>Tell me about your business idea.</p>
                     <p>I'll help you refine it for better branding.</p>
                 </div>
             )}
             {messages.map((m, i) => (
                 <div key={i} className={`flex ${m.role === 'user' ? 'justify-end' : 'justify-start'}`}>
                     <div className={`max-w-[85%] p-3 rounded-xl text-sm ${m.role === 'user' ? 'bg-indigo-600 text-white rounded-tr-none' : 'bg-white border border-slate-200 text-slate-700 rounded-tl-none'}`}>
                         {m.text}
                     </div>
                 </div>
             ))}
             {isLoading && <Loader2 className="w-5 h-5 text-slate-400 animate-spin" />}
             <div ref={chatEndRef} />
          </div>

          <div className="p-3 border-t bg-white">
              <div className="flex items-end gap-2">
                  <textarea 
                    ref={textareaRef}
                    className="flex-1 text-sm bg-slate-100 rounded-lg px-3 py-2 outline-none resize-none max-h-32"
                    placeholder="Ask for advice..."
                    value={input}
                    rows={1}
                    onChange={handleInput}
                    onKeyDown={e => {
                        if (e.key === 'Enter' && !e.shiftKey) {
                            e.preventDefault();
                            sendMessage();
                        }
                    }}
                  />
                  <button onClick={sendMessage} disabled={isLoading} className="p-2 mb-0.5 bg-indigo-600 text-white rounded-lg">
                      <MessageSquare className="w-4 h-4" />
                  </button>
              </div>
          </div>
        </div>
      )}
    </div>
  );
};

