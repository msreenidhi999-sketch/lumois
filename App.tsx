const simpleHash = (str: string) => {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    const char = str.charCodeAt(i);
    hash = (hash << 5) - hash + char;
    hash = hash & hash;
  }
  return hash.toString();
};

const exportToPDF = (project: BrandProject) => {
  const doc = new jsPDF();
  const d = project.generatedData;
  if (!d) return;

  const margin = 20;
  let y = 20;
  const pageWidth = doc.internal.pageSize.getWidth();
  const contentWidth = pageWidth - (margin * 2);

  const addText = (text: string, size: number, style: 'normal' | 'bold' = 'normal', color: [number, number, number] = [40, 40, 40]) => {
    doc.setFontSize(size);
    doc.setFont("helvetica", style);
    doc.setTextColor(color[0], color[1], color[2]);
    const lines = doc.splitTextToSize(text, contentWidth);
    
    if (y + (lines.length * size * 0.4) > 280) {
        doc.addPage();
        y = 20;
    }
    
    doc.text(lines, margin, y);
    y += (lines.length * size * 0.4) + 6;
  };

  const addSectionTitle = (title: string) => {
      y += 10;
      addText(title, 18, 'bold', [79, 70, 229]); // Indigo color
      y += 2;
  };

  // Title Page
  doc.setFontSize(40);
  doc.setTextColor(79, 70, 229);
  doc.text(project.name || "Brand Identity", pageWidth / 2, 100, { align: 'center' });
  doc.setFontSize(14);
  doc.setTextColor(100, 100, 100);
  doc.text("Generated by BrandForge AI", pageWidth / 2, 115, { align: 'center' });
  doc.text(new Date().toLocaleDateString(), pageWidth / 2, 125, { align: 'center' });
  doc.addPage();
  y = 20;

  // Overview
  addSectionTitle("Brand Overview");
  addText(`Industry: ${project.industry}`, 12);
  addText(`Tone: ${project.tone}`, 12);
  addText(`Taglines:`, 14, 'bold');
  d.taglines.forEach(t => addText(`• ${t}`, 11));
  
  // Strategy
  addSectionTitle("Strategic Core");
  addText("Vision", 14, 'bold');
  addText(d.story.vision, 11);
  addText("Mission", 14, 'bold');
  addText(d.story.mission, 11);
  addText("Positioning", 14, 'bold');
  addText(d.story.positioning, 11);
  addText("Problem & Solution", 14, 'bold');
  addText(`Problem: ${d.story.problem}`, 11);
  addText(`Solution: ${d.story.solution}`, 11);

  // Visuals
  doc.addPage();
  y = 20;
  addSectionTitle("Visual Identity");
  
  // Colors
  addText("Primary Colors", 14, 'bold');
  const palette = d.visuals.palettes[d.visuals.selectedPaletteIndex || 0];
  let xOffset = margin;
  palette.colors.forEach((c) => {
      doc.setFillColor(c);
      doc.rect(xOffset, y, 30, 30, 'F');
      doc.setFontSize(10);
      doc.setTextColor(60, 60, 60);
      doc.text(c, xOffset, y + 36);
      xOffset += 35;
  });
  y += 50;

  // Typography
  addText("Typography", 14, 'bold');
  addText(`Logo Font: ${d.visuals.fonts.logo}`, 11);
  addText(`Heading Font: ${d.visuals.fonts.heading}`, 11);
  addText(`Body Font: ${d.visuals.fonts.body}`, 11);

  // Logo
  if (d.visuals.logoIconUrl) {
      addText("Logo Concept", 14, 'bold');
      try {
          doc.addImage(d.visuals.logoIconUrl, 'PNG', margin, y, 50, 50);
          addText(`Prompt: ${d.visuals.logoPrompt}`, 10);
          y += 60;
      } catch (e) {
          console.error("Could not add image to PDF", e);
      }
  }

  // Marketing
  doc.addPage();
  y = 20;
  addSectionTitle("Marketing Assets");
  addText("Descriptions", 14, 'bold');
  addText(d.marketing.shortDescription, 11);
  y += 5;
  addText(d.marketing.longDescription, 11);
  
  addText("Sample Email Copy", 14, 'bold');
  addText(d.marketing.emailCopy, 10);

  addText("Social Media Captions", 14, 'bold');
  d.marketing.socialCaptions.forEach(c => addText(`- ${c}`, 10));

  doc.save(`${project.name.replace(/\s+/g, '_')}_BrandKit.pdf`);
};

